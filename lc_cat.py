# ── 广期所 LC 交易时间 ────────────────────────────────────
def _is_trading():
    now = datetime.now()
    if now.weekday() >= 5:
        return False
    t = now.time()
    return (dtime(9, 0) <= t <= dtime(11, 30)) or (dtime(13, 30) <= t <= dtime(15, 0))

POS_FILE = os.path.expanduser("~/.lc_cat_pos")


class LCBubble:
    def __init__(self):
        self.root = tk.Tk()
        self.root.title("LC Bubble")

        sx, sy = 1000, 100
        try:
            if os.path.exists(POS_FILE):
                with open(POS_FILE) as f:
                    d = json.load(f)
                sx, sy = d.get("x", sx), d.get("y", sy)
        except Exception:
            pass

        self.root.geometry(f"{W}x{H}+{sx}+{sy}")
        self.root.overrideredirect(True)
        self.root.wm_attributes("-topmost", True)
        self.root.wm_attributes("-transparent", True)
        self.root.wm_attributes("-alpha", 0.78)   # 整体透明度（1.0=不透明，0=全透明）
        self.root.configure(bg=TRANSPARENT)

        self.canvas = tk.Canvas(
            self.root, width=W, height=H,
            bg=TRANSPARENT, highlightthickness=0
        )
        self.canvas.pack()

        # ── 数据状态 ──
        self.lc_symbol   = "LC2609"
        self.price_data  = None
        self.last_update = ""
        # 每日语录：按日期 seed 打乱，取前 100 条
        self._current_date   = None
        self._daily_indices  = []
        self._quote_pos      = 0
        self._init_daily_quotes()

        # ── 拖拽 ──
        self._drag_ox = self._drag_oy = 0
        self.canvas.bind("<Button-1>",        self._on_drag_start)
        self.canvas.bind("<B1-Motion>",       self._on_drag)
        self.canvas.bind("<ButtonRelease-1>", self._on_drag_end)

        # ── 启动 ──
        self._start_data_thread()
        self._tick()

    # ── 每日语录初始化（滑窗算法：每天步进100，相邻两天无重叠）──
    # 用固定种子42打乱一次，作为稳定的基础顺序
    _STABLE_ORDER = None

    @classmethod
    def _get_stable_order(cls):
        if cls._STABLE_ORDER is None:
            indices = list(range(len(QUOTES)))
            random.Random(42).shuffle(indices)
            cls._STABLE_ORDER = indices
        return cls._STABLE_ORDER

    def _init_daily_quotes(self):
        today = datetime.now().date()
        if today != self._current_date:
            self._current_date = today
            base   = datetime(2025, 1, 1).date()
            day_no = (today - base).days          # 从基准日算第几天
            N      = len(QUOTES)
            order  = self._get_stable_order()
            step   = 100                           # 每天步进 100 → 相邻天零重叠
            start  = (day_no * step) % N
            # 取连续100条（带循环）
            self._daily_indices = [order[(start + i) % N] for i in range(100)]
            self._quote_pos = 0

    # ── 数据线程 ──────────────────────────────────────────
    def _start_data_thread(self):
        def run():
            # 先获取主力合约代码
            try:
                contracts = ak.match_main_contract(symbol="gfex")
                lc = next((s for s in contracts.split(",") if s.startswith("LC")), None)
                if lc:
                    self.lc_symbol = lc
            except Exception:
                pass
            # 首次获取
            self._fetch()
            # 每 60 秒更新一次
            while True:
                time.sleep(60)
                self._fetch()

        threading.Thread(target=run, daemon=True).start()

    def _fetch(self):
        """更新行情 + 切换语录（每日100条循环，日期变化时更新语录集）"""
        # 检查日期变化（跨天时重新生成当日100条）
        self._init_daily_quotes()
        # 切换到下一条语录
        self._quote_pos = (self._quote_pos + 1) % 100

        if _is_trading():
            try:
                df  = ak.futures_zh_spot(symbol=self.lc_symbol, market="CF", adjust="0")
                row = df.iloc[0]
                curr = float(row["current_price"])
                prev = float(row["last_settle_price"])
                self.price_data = {
                    "symbol": self.lc_symbol,
                    "price":  curr,
                    "pct":    (curr - prev) / prev * 100 if prev else 0.0,
                }
            except Exception as e:
                print(f"[LC] fetch error: {e}")
        else:
            self.price_data = None

        self.last_update = datetime.now().strftime("%H:%M")

    # ── 动画 / 重绘 ────────────────────────────────────────
    def _tick(self):
        self._draw()
        self.root.after(200, self._tick)   # 200ms 重绘（低频即可）

    def _draw(self):
        c = self.canvas
        c.delete("all")

        # ── 气泡背景（圆角矩形）──
        r = 10
        self._round_rect(c, 2, 2, W-2, H-2, r,
                         fill=BG, outline=BORDER, width=1.5)

        # ── 关闭按钮 ──
        close_x, close_y = W-16, 12
        c.create_oval(close_x-8, close_y-8, close_x+8, close_y+8,
                      fill="#F0E0A0", outline=BORDER, width=1)
        c.create_text(close_x, close_y, text="×",
                      font=("Arial", 10, "bold"), fill=CLOSE_C)

        # ── 固定布局位置 ──
        Q_Y    = 14   # 语录顶部
        AUTH_Y = 56   # 作者+价格行

        # ── 投资语录（按当日索引）──
        real_idx = self._daily_indices[self._quote_pos] if self._daily_indices else 0
        q_text, q_author = QUOTES[real_idx]
        c.create_text(14, Q_Y, text=f"「{q_text}」",
                      anchor="nw", font=("PingFang SC", 10),
                      fill=QUOTE_C, width=W-28)

        # ── 作者 + 价格（同字体同样式，无千分位，隐于署名后）──
        if self.price_data:
            price_suffix = f"  {int(self.price_data['price'])}"
        else:
            price_suffix = ""
        c.create_text(W-14, AUTH_Y,
                      text=f"—— {q_author}{price_suffix}",
                      anchor="ne",
                      font=("PingFang SC", 9, "italic"),
                      fill=AUTHOR_C)

    @staticmethod
    def _round_rect(canvas, x1, y1, x2, y2, r, **kw):
        pts = [
            x1+r, y1,   x2-r, y1,
            x2,   y1,   x2,   y1+r,
            x2,   y2-r, x2,   y2,
            x2-r, y2,   x1+r, y2,
            x1,   y2,   x1,   y2-r,
            x1,   y1+r, x1,   y1,
        ]
        return canvas.create_polygon(pts, smooth=True, **kw)

    # ── 交互 ──────────────────────────────────────────────
    def _on_drag_start(self, event):
        # 点击关闭按钮区域（右上角圆圈）
        if (W-26 <= event.x <= W-6) and (3 <= event.y <= 22):
            self._on_close()
            return
        self._drag_ox, self._drag_oy = event.x, event.y

    def _on_drag(self, event):
        x = self.root.winfo_x() + event.x - self._drag_ox
        y = self.root.winfo_y() + event.y - self._drag_oy
        self.root.geometry(f"+{x}+{y}")

    def _on_drag_end(self, event):
        pass

    def _on_close(self, event=None):
        try:
            with open(POS_FILE, "w") as f:
                json.dump({"x": self.root.winfo_x(),
                           "y": self.root.winfo_y()}, f)
        except Exception:
            pass
        self.root.quit()

    def run(self):
        self.root.mainloop()


if __name__ == "__main__":
    LCBubble().run()
